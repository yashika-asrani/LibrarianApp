export default Object.freeze({
    APP_NAME: "Librarian"
})

// Object.freeze is a built-in method in JavaScript that can be used to make an object immutable, which means its properties cannot be changed, added or deleted. 

// export default is used to export the object as a module, which can be imported and used in other parts of a JavaScript program. 

// In this case, the module only exports one immutable property APP_NAME with a string value of "Librarian", which can be used in other parts of the program without the risk of accidentally modifying its value.